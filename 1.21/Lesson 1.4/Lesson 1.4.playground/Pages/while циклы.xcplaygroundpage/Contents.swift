import Foundation

//: ## While циклы

/*:
 
    while condition {
        some code
    }
 
 */

var counter = 5

while counter > 0 {
    print("Hello")
    counter -= 1
}

counter = 5
/*:
 
    repeat {
        some code
    } while condition
 
 */

repeat {
    print("Hello")
    counter -= 1
} while counter > 0

